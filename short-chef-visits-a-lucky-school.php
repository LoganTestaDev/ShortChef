<h3>Short Chef Visits a Lucky School</h3>


<div class="content8">
<p>Team 1 held shiny yellow lemons in their fists and pumped them in the air. Not to be outdone, Team 2 showed off their cans of hearts of palm: "Look, look, we've got ours!" 
At just that moment, Team 3 came barreling down the aisle bearing plastic tins of organic field greens, and Team 4 rounded the corner with their cherry berry tomatoes. 
Cristopher, 10, held a papaya up for everyone to see.</p>
<p>&nbsp;</p>
<p>But where was Team 5 with the olive oil and balsamic vinegar?</p>
<p>&nbsp;</p>
<p>They showed up right on time, along with all the teams, to begin to prepare lunch in the Culinary Center of the Whole Foods Market in Coral Gables. For this second day of 
their special nutrition class with "Short Chef" Ray Newlands, the 25 children of the City of Hialeah Victor Wilde Community Center summer camp visited the huge natural foods 
market, the sponsor for the nutrition program.</p>
<p>&nbsp;</p>
<p>The City of Hialeah has been implementing Creative Learning & Play (CL&P) programs with Children's Trust funding since 2006. For the nutrition class, children 6-12 learn 
that healthy eating is mostly about making good food choices, and that fitness and diet go hand in hand. More than 400 children will take "Short Chef's" class this summer at 
six different locations over the 11 weeks of the camp. An expanded version is planned for the after-school program in the fall.</p>
<p>&nbsp;</p>
<p>"We're talking at the kids' level without forcing the idea of healthy eating down their throats. We're being silly and helping to change this obesity problem – you can't 
talk enough about it," said Newlands, a professional chef. "We give them something to believe in."</p>
<p>&nbsp;</p>
<p>Hialeah administrators Program Director Idi Blanco, Literacy Director Maria Gomez, and Site Director Robert Lopez couldn't be more excited about the nutritional classes.</p>
<p>&nbsp;</p>
<p>"The kids just love the class. They have so much fun – and they're learning things that will help change their habits and their health," Gomez said. "They take this learning 
home and tell their parents – it's fabulous." The first day is a two-hour interactive presentation. Newlands brings an assortment of fruits and vegetables to emphasize their 
importance in the children's diet. They pass them around and talk about where they're from. "Everything you see here you can grow in your house or in your garden," he says.</p>
<p>&nbsp;</p>
<p>Newlands knows that fast food is popular with the children, but he talks with them about the risks, especially later in life, that come from a diet high in sugar, fat, salt 
and saturated oils. Many of the kids are well aware.</p>
<p>&nbsp;</p>
<p>"My mom said I might have a heart attack when I'm a teen unless I cut down on my McDonald's," one girl says.</p>
<p>&nbsp;</p>
<p>"Short Chef" keeps the class lively with stories and jokes, yet he's honest with the children, too. He talks openly about the angioplasty surgery that saved his life.</p>
<p>&nbsp;</p>
<p>Day Two of the class is a field trip to Whole Foods. The kids climb off their bus and enter the store's state-of-the-art Culinary Center, a modern sparkling kitchen. 
Stations have been set for the kids: cups filled with samples of lemon slices, capers, honey, and organic bitter chocolate.  
The kids start with a review of the previous day's session. They remember the food pyramid and the different fruits and vegetables they learned about the day before.</p>
<p>&nbsp;</p>
<p>Then it's time for some tasting. "Short Chef" emphasizes that eating is an experience involving all the senses. The kids hold the foods in their hands, smelling them, 
and then tasting. They scrunch their faces, grimace and some groan. They don't like all the tastes, but they all try the new foods – and many, they do like.</p>
<p>&nbsp;</p>
<p>"My point to you, special people, is if you get a chance to try something new, try it," Short Chef tells them.</p>
<p>&nbsp;</p>
<p>Then it's time to tour the store. Marketing Director Natalie White leads the children out into the 48,000-square-foot facility and, as they swing out of the double-doors, 
their eyes widen – and widen. They've never seen a store like this.</p>
<p>&nbsp;</p>
<p>They start by the rows of organic vegetables. White explains that the foods have to be grown free of pesticides and chemicals. At the seafood case, they gawk at the slabs 
of fresh fish, giant prawns and octopus. White seizes the moment to teach about "sustainability."</p>
<p>&nbsp;</p>
<p>She positions a few of the children as different fishing grounds. "What happens," White asks, "if every day you fish in this one place?"</p>
<p>&nbsp;</p>
<p>"Soon all the fish are gone," one child offers.</p>
<p>&nbsp;</p>
<p>"And if you fish here one day, then over here the next, and back here again?"</p>
<p>&nbsp;</p>
<p>"The same, pretty soon all the fish will disappear."</p>
<p>&nbsp;</p>
<p>The children quickly get her message about sustainability – that it's important not to abuse world resources, like fishing, but instead to use them carefully over time.</p>
<p>&nbsp;</p>
<p>The tour continues of the many different sections of the store, and at the end, White gives them an oral quiz: The kids get all the answers correct.</p>
<p>&nbsp;</p>
<p>They return to the Culinary Center to make their lunch. Each team has located their ingredients and one at a time, they add them to the huge metal salad bowl.</p>
<p>&nbsp;</p>
<p>The crowning moment has arrived. It's time to add the dressing to the field greens, hearts of palm, cherry tomatoes, slices of papaya and oranges and herbs.</p>
<p>&nbsp;</p>
<p>"Are you nervous about this Emily?" Newlands asks one of the girls. Without waiting for a reply, he answers: "Me, too!"</p>
<p>&nbsp;</p>
<p>Emily and a few other children dig into the bowl, their hands in plastic gloves. They turn and swirl the dressing through the mixture. Everyone gets a plate of salad – 
and nearly everyone eats theirs.</p>
<p>&nbsp;</p>
<p>Cristopher, a 10-year-old whose hair is bunched like a rooster comb on the top of his head, is finishing his salad. "What are you going to tell your parents about your visit here today?"</p>
<p>&nbsp;</p>
<p>"I'm going to say that I ate this," he answers, pointing proudly to the empty salad plate.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p>Written by Michael R. Malone</p>
